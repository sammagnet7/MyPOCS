package com.bagic.Channelservices.legacy.controller;

public class ExcludedController {
	
	private int demoInt;
	
	private String demoString;

	public ExcludedController(int demoInt, String demoString) {
		super();
		this.demoInt = demoInt;
		this.demoString = demoString;
	}

	public int getDemoInt() {
		return demoInt;
	}

	public void setDemoInt(int demoInt) {
		this.demoInt = demoInt;
	}

	public String getDemoString() {
		return demoString;
	}

	public void setDemoString(String demoString) {
		this.demoString = demoString;
	}
	
	

}
