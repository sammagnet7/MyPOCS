package com.bagic.Channelservices;

public class ExcludedLegacyClass {
	
	public int abc;
	public int bcd;
	
	
	public ExcludedLegacyClass(int abc, int bcd) {
		super();
		this.abc = abc;
		this.bcd = bcd;
	}
	public int getAbc() {
		return abc;
	}
	public void setAbc(int abc) {
		this.abc = abc;
	}
	public int getBcd() {
		return bcd;
	}
	public void setBcd(int bcd) {
		this.bcd = bcd;
	}
	
	

}
